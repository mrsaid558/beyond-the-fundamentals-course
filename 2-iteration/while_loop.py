'''
While loop
    - we use while loops to continue a loop until the program arrives at a certain state.
'''

i = 5

print("count to 100 by fives")
while i <= 100:
    print(i)
    i += 5
    
x = 10

print("-" * 30)
print("count from 10 to 30 by 2")

while x <= 30:
    print(x)
    x += 2